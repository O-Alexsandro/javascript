// Tipos de função

// Declarativas
function funcao (){
    console.log('ok');
}
funcao();

// Expressões de funções
var funcao1 = function(){
    console.log('Sou uma mensagem de função de expressão');
}
funcao1();

// Arrow Function
var arrow = () => {
    console.log('Sou uma arrow function');
}
arrow();

